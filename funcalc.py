def multiplicacao(n1, n2):      #função que calcula e retorna o resultade de uma multiplicação
    return n1 * n2

def divisao(n1, n2):            #funcção que calcula e retorna o resultado de uma divisão
    return n1 / n2

def soma(n1, n2):               #função que calcula e retorna o resultado de uma adição
    return n1 + n2

def subtracao(n1, n2):          #função que calcula e retorna o resultado de uma subtração
    return n1 - n2

def porcentagem(n1, n2):        #função para cálculo e retorno da porcentagem
    return ((n1 * n2) / 100)